    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        int getAnswer = JOptionPane.showConfirmDialog(rootPane, "Apakah yakin ingin menghapus data ?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        switch(getAnswer){
            case 0:
                try{
                    karyawanControl.deleteKaryawan(Integer.parseInt(inputId.getText()));
                    clearText();
                    showKaryawan();
                    setComponent(false);
                    setEditDeleteBtn(false);                    
                    JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus!");
                }catch(Exception e){
                    System.out.println("Error : " +e.getMessage());
                }
                break;
            case 1:
                JOptionPane.showMessageDialog(null, "Batal Menghapus Data!");
                break;
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void karyawanTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_karyawanTableMouseClicked
        setEditDeleteBtn(true);
        setComponent(false);
        
        int clickedRow = karyawanTable.getSelectedRow();
        TableModel tableModel = karyawanTable.getModel();
        
        inputId.setText(tableModel.getValueAt(clickedRow, 0).toString());
        inputNama.setText(tableModel.getValueAt(clickedRow, 1).toString());
        inputUsia.setText(tableModel.getValueAt(clickedRow, 2).toString());
        inputAlamat.setText(tableModel.getValueAt(clickedRow, 3).toString());
        String jenis = tableModel.getValueAt(clickedRow, 4).toString();
        inputNoTelp.setText(tableModel.getValueAt(clickedRow, 5).toString());
        
        switch(jenis){
            case "Admin":
                adminRBtn.setSelected(true);
                managerRBtn.setSelected(false);
                break;
            case "Manager":
                managerRBtn.setSelected(true);
                adminRBtn.setSelected(false);
                break;
            default :
                managerRBtn.setSelected(false);
                adminRBtn.setSelected(false);
                break;
        }        
        cancelBtn.setEnabled(true);        
    }//GEN-LAST:event_karyawanTableMouseClicked

    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        setEditDeleteBtn(true);
        setComponent(false);
        
        try{
           TableKaryawan tk = karyawanControl.showKaryawan(searchInput.getText());
            if(tk.getRowCount()==0) {
                clearText();
                setEditDeleteBtn(false);
                JOptionPane.showConfirmDialog(null, "Data Tidak Ditemukan!", "Konfirmasi", JOptionPane.DEFAULT_OPTION);                
            }else{
                karyawanTable.setModel(tk);
            }
            clearText();
            cancelBtn.setEnabled(true);
        }catch(Exception e){
            System.out.println("Error : " + e.getMessage());
        }
    }//GEN-LAST:event_searchBtnActionPerformed

