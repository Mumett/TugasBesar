    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try{
            InputKosongException();
            jenisKaryawanException();
            
            Karyawan k = null;
            if(adminRBtn.isSelected()==true){
                String jenis = "Admin";
                k = new Karyawan(inputNama.getText(), inputAlamat.getText(), jenis, inputNoTelp.getText(), Integer.parseInt(inputId.getText()), Integer.parseInt(inputUsia.getText()));
            }else{
                String jenis = "Manager";
                k = new Karyawan(inputNama.getText(), inputAlamat.getText(), jenis, inputNoTelp.getText(), Integer.parseInt(inputId.getText()), Integer.parseInt(inputUsia.getText()));
            }
            if(action.equals("Tambah")){
                karyawanControl.insertKaryawan(k);
                JOptionPane.showMessageDialog(null, "Data Berhasil Ditambahkan !");
            }else{
                karyawanControl.updateKaryawan(k,Integer.parseInt(inputId.getText()));
                JOptionPane.showMessageDialog(null, "Data Berhasil Diubah !");
            }
            clearText();
            setComponent(false);
            setEditDeleteBtn(false);
            showKaryawan();
        }catch(InputKosongException e){
            JOptionPane.showMessageDialog(this, e.message());
        }catch(JenisKaryawanException e1){
            JOptionPane.showMessageDialog(this, e1.message());
        }catch(NumberFormatException e2){
            JOptionPane.showMessageDialog(this, "Input Invalid !");
        }
    }//GEN-LAST:event_saveBtnActionPerformed

