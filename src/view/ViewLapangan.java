    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        int getAnswer = JOptionPane.showConfirmDialog(rootPane, "Apakah yakini ingin menghapus data?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        
        if (getAnswer == JOptionPane.YES_NO_OPTION){
            try{
                lapanganControl.deleteDataLapangan(Integer.parseInt(inputId.getText()));
                clearText();
                showDataLapangan();
                setEditDeleteBtn(false);
                JOptionPane.showMessageDialog(null, " Data berhasil dihapus!");
            }catch(Exception e){
                System.out.println(e);}
        }else{
            JOptionPane.showMessageDialog(null,  "Data tidak jadi dihapus!");
            setComponent(false);
            showDataLapangan();
            setEditDeleteBtn(false);
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try{
            String jenis = "";
            String fasilitas = "";
            String status = "";
            
            InputKosongException();
            jenisLapanganException();
            statusLapanganException();            
            
            if(sintetisRBtn.isSelected() == true){
                jenis = "Sintetis";                
            }else if(plurRBtn.isSelected()==true){
                jenis = "Plur";
            }
             if(availableRBtn.isSelected() == true){
                 status = "Available";
             }else if(bookedRBtn.isSelected() == true){
                 status = "Booked";
             }
             
             if(!bolaCBox.isSelected() && !rompiCBox.isSelected() && !airMineralCBox.isSelected()){
                 fasilitas = "-,-,-";
             }else if(bolaCBox.isSelected() && !rompiCBox.isSelected() && !airMineralCBox.isSelected()){
                 fasilitas = "Bola,-,-";
             }else if(!bolaCBox.isSelected() && rompiCBox.isSelected() && !airMineralCBox.isSelected()){
                 fasilitas = "-,Rompi,-";
             }else if(!bolaCBox.isSelected() && !rompiCBox.isSelected() && airMineralCBox.isSelected()){
                 fasilitas = "-,-,Air Mineral";
             }else if(bolaCBox.isSelected() && rompiCBox.isSelected() && !airMineralCBox.isSelected()){
                 fasilitas = "Bola,Rompi,-";
             }else if(bolaCBox.isSelected() && !rompiCBox.isSelected() && airMineralCBox.isSelected()){
                 fasilitas = "Bola,-,Air Mineral";
             }else if(!bolaCBox.isSelected() && rompiCBox.isSelected() && airMineralCBox.isSelected()){
                 fasilitas = "-,Rompi,Air Mineral";
             }else if(bolaCBox.isSelected() && rompiCBox.isSelected() && airMineralCBox.isSelected()){
                 fasilitas = "Bola,Rompi,Air Mineral";
             }
            
            if(action.equals("Tambah")){
                Lapangan l = new Lapangan(Integer.parseInt(inputId.getText()), inputNama.getText(), Integer.parseInt(inputLuas.getText()), jenis, fasilitas, status);
                lapanganControl.insertDataLapangan(l);
                JOptionPane.showMessageDialog(null, "Data Berhasil Ditambahkan");
            }else{
                Lapangan l = new Lapangan(selected, inputNama.getText(), Integer.parseInt(inputLuas.getText()), jenis, fasilitas, status);
                lapanganControl.updateDataLapangan(l,Integer.parseInt(inputId.getText()));
                JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
            }
            setComponent(false);
            setEditDeleteBtn(false);
            clearText();    
            showDataLapangan();
        }catch(InputKosongException e){
            JOptionPane.showMessageDialog(this, e.message());
        }catch(JenisLapanganException e1){
            JOptionPane.showMessageDialog(this, e1.message());
        }catch(StatusLapanganException e2){
            JOptionPane.showMessageDialog(this, e2.message());
        }catch(NumberFormatException e3){
            JOptionPane.showMessageDialog(this, "Input Invalid !");
        }
    }//GEN-LAST:event_saveBtnActionPerformed

