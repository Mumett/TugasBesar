    private void lapanganTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lapanganTableMouseClicked
        setEditDeleteBtn(true);
        setComponent(false);
        
        int clickedRow = lapanganTable.getSelectedRow();
        TableModel tableModel = lapanganTable.getModel();
        
        inputId.setText(tableModel.getValueAt(clickedRow, 0).toString());
        inputNama.setText(tableModel.getValueAt(clickedRow, 1).toString());
        inputLuas.setText(tableModel.getValueAt(clickedRow, 2).toString());
        String jenis = tableModel.getValueAt(clickedRow, 3).toString();
        String fasilitas = tableModel.getValueAt(clickedRow, 4).toString();
        String status = tableModel.getValueAt(clickedRow, 5).toString();
        
        switch(jenis){
            case "Sintetis":
                sintetisRBtn.setSelected(true);
                plurRBtn.setSelected(false);
                break;
            case "Plur":
                sintetisRBtn.setSelected(false);
                plurRBtn.setSelected(true);
                break;
            default :
                sintetisRBtn.setSelected(false);
                plurRBtn.setSelected(false);
                break;
        }
        
        if(!bolaCBox.isSelected() && !rompiCBox.isSelected() && !airMineralCBox.isSelected()){
            bolaCBox.setSelected(false);
            rompiCBox.setSelected(false);
            airMineralCBox.setSelected(false);
        }else if(bolaCBox.isSelected() && !rompiCBox.isSelected() && !airMineralCBox.isSelected()){
            bolaCBox.setSelected(true);
            rompiCBox.setSelected(false);
            airMineralCBox.setSelected(false);
        }else if(!bolaCBox.isSelected() && rompiCBox.isSelected() && !airMineralCBox.isSelected()){
            bolaCBox.setSelected(false);
            rompiCBox.setSelected(true);
            airMineralCBox.setSelected(false);
        }else if(!bolaCBox.isSelected() && !rompiCBox.isSelected() && airMineralCBox.isSelected()){
            bolaCBox.setSelected(false);
            rompiCBox.setSelected(false);
            airMineralCBox.setSelected(true);
        }else if(bolaCBox.isSelected() && rompiCBox.isSelected() && !airMineralCBox.isSelected()){
            bolaCBox.setSelected(true);
            rompiCBox.setSelected(true);
            airMineralCBox.setSelected(false);
        }else if(bolaCBox.isSelected() && !rompiCBox.isSelected() && airMineralCBox.isSelected()){
            bolaCBox.setSelected(true);
            rompiCBox.setSelected(false);
            airMineralCBox.setSelected(true);
        }else if(!bolaCBox.isSelected() && rompiCBox.isSelected() && airMineralCBox.isSelected()){
            bolaCBox.setSelected(false);
            rompiCBox.setSelected(true);
            airMineralCBox.setSelected(true);
        }else if(bolaCBox.isSelected() && rompiCBox.isSelected() && airMineralCBox.isSelected()){
            bolaCBox.setSelected(true);
            rompiCBox.setSelected(true);
            airMineralCBox.setSelected(true);
        }
        
        switch(status){
            case "Available":
                availableRBtn.setSelected(true);
                bookedRBtn.setSelected(false);
                break;
            case "Booked":
                availableRBtn.setSelected(false);
                bookedRBtn.setSelected(true);
                break;
            default :
                availableRBtn.setSelected(false);
                bookedRBtn.setSelected(false);
                break;
        }
        
        cancelBtn.setEnabled(true);
    }//GEN-LAST:event_lapanganTableMouseClicked
   
